package com.xunmaw.graduate.utils;

import java.io.Serializable;

public class Situation implements Serializable {
    private double gy;
    private double my;
    private double wz;
    private double zz;
    private double ky;
    private double gx;
    private double qt;

    public double getGy() {
        return gy;
    }

    public void setGy(double gy) {
        this.gy = gy;
    }

    public double getMy() {
        return my;
    }

    public void setMy(double my) {
        this.my = my;
    }

    public double getWz() {
        return wz;
    }

    public void setWz(double wz) {
        this.wz = wz;
    }

    public double getZz() {
        return zz;
    }

    public void setZz(double zz) {
        this.zz = zz;
    }

    public double getKy() {
        return ky;
    }

    public void setKy(double ky) {
        this.ky = ky;
    }

    public double getGx() {
        return gx;
    }

    public void setGx(double gx) {
        this.gx = gx;
    }

    public double getQt() {
        return qt;
    }

    public void setQt(double qt) {
        this.qt = qt;
    }
}
